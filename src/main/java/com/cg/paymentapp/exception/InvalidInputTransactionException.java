package com.cg.paymentapp.exception;

public class InvalidInputTransactionException extends Exception {

	public InvalidInputTransactionException() {
		super();
		
	}

	public InvalidInputTransactionException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
		
	}

	public InvalidInputTransactionException(String message, Throwable cause) {
		super(message, cause);
		
	}

	public InvalidInputTransactionException(String message) {
		super(message);
		
	}

	public InvalidInputTransactionException(Throwable cause) {
		super(cause);
		
	}
	

}
